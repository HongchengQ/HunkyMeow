package emu.grasscutter.data.common;

public class PointsInfo {
    public double x;
    public double y;

    public PointsInfo(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getx() {
        return x;
    }
    public double gety() {
        return y;
    }
}
